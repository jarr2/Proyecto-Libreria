class Libro():

    def __init__(self, id_libro, nombre, editorial, autor, stock, estatus, precio, img_ruta):
        self.id_libro = id_libro
        self.nombre = nombre
        self.editorial = editorial
        self.autor = autor
        self.stock = stock
        self.estatus = estatus
        self.precio = precio
        self.img_ruta = img_ruta

