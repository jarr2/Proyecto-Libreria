class Usuario():

    def __init__(self, id_usuario, id_direccion, id_login, nombre, apellidos, numero_telefonico):
        self.id_usuario = id_usuario
        self.id_direccion = id_direccion
        self.id_login = id_login
        self.nombre = nombre
        self.apellidos = apellidos
        self.numero_telefonico = numero_telefonico
